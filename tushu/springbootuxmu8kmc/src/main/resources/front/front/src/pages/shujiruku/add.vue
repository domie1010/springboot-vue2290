<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            jieyuehao : false,
            bookname : false,
            shujileixing : false,
            chubanshe : false,
            chubanyear : false,
            shuliang : false,
            rukudengji : false,
            rukuren : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          jieyuehao: '',
          bookname: '',
          shujileixing: '',
          chubanshe: '',
          chubanyear: '',
          shuliang: '',
          rukudengji: '',
          rukuren: '',
        },
        jieyuehaoOptions: [],


        rules: {
          jieyuehao: [
          ],
          bookname: [
            { required: true, message: '书名不能为空', trigger: 'blur' },
          ],
          shujileixing: [
            { required: true, message: '书籍类型不能为空', trigger: 'blur' },
          ],
          chubanshe: [
          ],
          chubanyear: [
          ],
          shuliang: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          rukudengji: [
          ],
          rukuren: [
            { required: true, message: '入库人不能为空', trigger: 'blur' },
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.rukudengji = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='jieyuehao'){
              this.ruleForm.jieyuehao = obj[o];
              this.ro.jieyuehao = true;
              continue;
            }
            if(o=='bookname'){
              this.ruleForm.bookname = obj[o];
              this.ro.bookname = true;
              continue;
            }
            if(o=='shujileixing'){
              this.ruleForm.shujileixing = obj[o];
              this.ro.shujileixing = true;
              continue;
            }
            if(o=='chubanshe'){
              this.ruleForm.chubanshe = obj[o];
              this.ro.chubanshe = true;
              continue;
            }
            if(o=='chubanyear'){
              this.ruleForm.chubanyear = obj[o];
              this.ro.chubanyear = true;
              continue;
            }
            if(o=='shuliang'){
              this.ruleForm.shuliang = obj[o];
              this.ro.shuliang = true;
              continue;
            }
            if(o=='rukudengji'){
              this.ruleForm.rukudengji = obj[o];
              this.ro.rukudengji = true;
              continue;
            }
            if(o=='rukuren'){
              this.ruleForm.rukuren = obj[o];
              this.ro.rukuren = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
          }
        });
        this.$http.get('option/tushuguihai/jieyuehao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.jieyuehaoOptions = res.data.data;
          }
        });
      },
      // 下二随
      jieyuehaoChange () {
        this.$http.get('follow/tushuguihai/jieyuehao?columnValue=' + this.ruleForm.jieyuehao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.shuliang){
              this.ruleForm.shuliang = res.data.data.shuliang
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`shujiruku/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('shujiruku/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post(`shujiruku/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post(`shujiruku/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

